TEST(KMIPResponseParsing, GetAttributesState) {
    // 1. Check the state enum for the "State" attribute reads preActive.
    const SecureVector<char> getAttributesPreActiveState(_getAttributesState.begin(),
                                                         _getAttributesState.end());
    StatusWith<KMIPResponse> swGetAttributesResponse =
        KMIPResponse::create(getAttributesPreActiveState);
    ASSERT(swGetAttributesResponse.isOK()) << "Failed to parse get attribute response "
                                           << swGetAttributesResponse.getStatus().toString();

    KMIPResponse r = std::move(swGetAttributesResponse.getValue());
    std::string expectedUID = "61b10614-d8b5-46f9-8d17-2fa6ea1d747a";
    ASSERT(expectedUID == r.getUID()) << "UID mismatch " << expectedUID << " != " << r.getUID();

    KMIPResponse::Attribute expectedState = {"State", 0, StateName::preActive};
    KMIPResponse::Attribute parsedAttribute = r.getAttribute();

    ASSERT(parsedAttribute.name == expectedState.name)
        << "Attribute Name mismatch " << expectedState.name << " != " << parsedAttribute.name;
    ASSERT(parsedAttribute.index == expectedState.index)
        << "Attribute Index mismatch " << expectedState.index << " != " << parsedAttribute.index;
    ASSERT(parsedAttribute.value.stateEnum == expectedState.value.stateEnum)
        << "State Enum mismatch " << unsigned(expectedState.value.stateEnum)
        << " != " << unsigned(parsedAttribute.value.stateEnum);

    // 2. Change the state enum for the "State" attribute to Active.
    _getAttributesState[kStateEnumIndex] = 0x02;
    const SecureVector<char> getAttributesActiveState(_getAttributesState.begin(),
                                                      _getAttributesState.end());
    swGetAttributesResponse = KMIPResponse::create(getAttributesActiveState);
    ASSERT(swGetAttributesResponse.isOK()) << "Failed to parse get attribute response "
                                           << swGetAttributesResponse.getStatus().toString();

    r = std::move(swGetAttributesResponse.getValue());
    expectedState.value.stateEnum = StateName::active;
    parsedAttribute = r.getAttribute();

    ASSERT(parsedAttribute.name == expectedState.name)
        << "Attribute Name mismatch " << expectedState.name << " != " << parsedAttribute.name;
    ASSERT(parsedAttribute.index == expectedState.index)
        << "Attribute Index mismatch " << expectedState.index << " != " << parsedAttribute.index;
    ASSERT(parsedAttribute.value.stateEnum == expectedState.value.stateEnum)
        << "State Enum mismatch " << unsigned(expectedState.value.stateEnum)
        << " != " << unsigned(parsedAttribute.value.stateEnum);
}
